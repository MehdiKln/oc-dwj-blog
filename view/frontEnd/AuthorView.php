Jean Forteroche
Je suis né le 6 juillet 1972 dans le 14e arrondissement de Paris, écrivain français, qui a obtenu le prix Goncourt des lycéens et le prix des libraires avec La Mort du roi Tsongor en 2003, puis le prix Goncourt pour mon roman Le Soleil des Scorta, en 2004.

Biographie
Il a étudié à l'École alsacienne2. Après une maîtrise de lettres à l'Université Paris III3, pour laquelle il a soutenu un mémoire intitulé "Le thème du combat dans la dramaturgie contemporaine française", sous la direction de Michel Corvin (1994), puis un DEA à la même université, pour lequel il a soutenu un mémoire intitulé Le conflit dans le théâtre contemporain, sous la direction de Jean-Pierre Sarrazac (1998), Laurent Gaudé écrit pour la scène (1999). Sa première pièce, intitulée Combat de possédés, paraît en 1999. Elle sera jouée en Allemagne et lue au Royal National Theatre de Londres3. La seconde pièce de Laurent Gaudé, publiée en 2000, est Onysos le Furieux. Il s'agit d'un monologue épique4, écrit en seulement 10 jours au printemps 19964. Laurent Gaudé a aussi écrit d'autres pièces de théâtre dont Pluie de Cendres, Cendres sur les mains, Médée Kali, ou encore Le Tigre bleu de l'Euphrate. En 2002, La Mort du roi Tsongor, son deuxième roman, lui vaut d'être cité pour le prix Goncourt et surtout d'être récompensé par le prix Goncourt des lycéens1 et le prix des libraires3. Deux ans plus tard, il remporte le prix Goncourt ainsi que le prix du jury Jean-Giono avec son roman Le Soleil des Scorta qui sera également un succès de librairie (80 000 exemplaires vendus entre la parution du roman et l'attribution du prix Goncourt en 20041).

